_cache: Dict[str, str] = {}
_cache_ts: float = 0.0


def _overrides() -> Dict[str, str]:
    """Mongo settings.personas override — 5초 캐시 (admin 수정 즉시 반영용)."""
    global _cache, _cache_ts
    now = time.time()
    if now - _cache_ts > 5:
        try:
            _cache = db.settings().find_one({"_id": "personas"}) or {}
        except Exception:
            _cache = {}
        _cache_ts = now
    return _cache


def current(key: str) -> str:
    """현재 적용값 — override 있으면 그것, 없으면 디폴트."""
    val = _overrides().get(key)
    return val if isinstance(val, str) and val.strip() else _DEFAULT[key]


# ── 역할별 시스템 프롬프트 (정체성 + 공통맥락 + 역할) — 각 모듈이 호출 ────
def _berr(role_key: str) -> str:
    """베르 = 정체성 + 공통맥락(산발 기록 등) + 역할."""
    return current("berr_identity") + current("berr_context") + current(role_key)


